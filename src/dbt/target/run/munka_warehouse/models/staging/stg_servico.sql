
  create or replace   view DRAGON_DB.munka_stg.stg_servico
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "NOME" COMMENT $$$$, 
  
    "ESCOPO" COMMENT $$$$, 
  
    "ENTREGAVEIS" COMMENT $$$$, 
  
    "ATIVIDADES" COMMENT $$$$, 
  
    "FATURAVEL" COMMENT $$$$, 
  
    "CONTRATO_ID" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    NULLIF(TRIM(NOME), '') AS NOME,
    NULLIF(TRIM(ESCOPO), '') AS ESCOPO,
    NULLIF(TRIM(ENTREGAVEIS), '') AS ENTREGAVEIS,
    NULLIF(TRIM(ATIVIDADES), '') AS ATIVIDADES,
    FATURAVEL,
    CONTRATO_ID,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_SERVICO
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

