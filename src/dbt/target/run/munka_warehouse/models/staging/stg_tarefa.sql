
  create or replace   view DRAGON_DB.munka_stg.stg_tarefa
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "NOME" COMMENT $$$$, 
  
    "DESCRICAO" COMMENT $$$$, 
  
    "CARGO_ID" COMMENT $$$$, 
  
    "NIVEL_ID" COMMENT $$$$, 
  
    "RESPONSAVEL_ID" COMMENT $$$$, 
  
    "DATA_INICIO" COMMENT $$$$, 
  
    "DATA_FIM" COMMENT $$$$, 
  
    "ETIQUETA_ID" COMMENT $$$$, 
  
    "PROJETO_ID" COMMENT $$$$, 
  
    "PRODUTO_ID" COMMENT $$$$, 
  
    "TIPO" COMMENT $$$$, 
  
    "NUM_HELPDESK" COMMENT $$$$, 
  
    "SPRINT_ID" COMMENT $$$$, 
  
    "REGRA_ID" COMMENT $$$$, 
  
    "STATUS_ID" COMMENT $$$$, 
  
    "HORAS_EXECUTADAS" COMMENT $$$$, 
  
    "EVIDENCIAS" COMMENT $$$$, 
  
    "EVIDENCIA_COMMIT_SHA" COMMENT $$$$, 
  
    "EVIDENCIA_ANEXO" COMMENT $$$$, 
  
    "FATURA_ID" COMMENT $$$$, 
  
    "VALOR_FATURADO" COMMENT $$$$, 
  
    "TOTAL_UST" COMMENT $$$$, 
  
    "DATA_ULTIMA_ATUALIZACAO" COMMENT $$$$, 
  
    "USER_ABERTURA_ID" COMMENT $$$$, 
  
    "DATA_ABERTURA" COMMENT $$$$, 
  
    "APROVADA" COMMENT $$$$, 
  
    "USER_APROVADA_ID" COMMENT $$$$, 
  
    "ID_TAREFA_OLD" COMMENT $$$$, 
  
    "USER_ULTIMA_ATUALIZACAO_ID" COMMENT $$$$, 
  
    "TAREFA_PAI_ID" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    NULLIF(TRIM(NOME), '') AS NOME,
    NULLIF(TRIM(DESCRICAO), '') AS DESCRICAO,
    CARGO_ID,
    NIVEL_ID,
    RESPONSAVEL_ID,
    DATA_INICIO,
    DATA_FIM,
    ETIQUETA_ID,
    PROJETO_ID,
    PRODUTO_ID,
    NULLIF(TRIM(TIPO), '') AS TIPO,
    NULLIF(TRIM(NUM_HELPDESK), '') AS NUM_HELPDESK,
    SPRINT_ID,
    REGRA_ID,
    STATUS_ID,
    HORAS_EXECUTADAS,
    NULLIF(TRIM(EVIDENCIAS), '') AS EVIDENCIAS,
    NULLIF(TRIM(EVIDENCIA_COMMIT_SHA), '') AS EVIDENCIA_COMMIT_SHA,
    NULLIF(TRIM(EVIDENCIA_ANEXO), '') AS EVIDENCIA_ANEXO,
    FATURA_ID,
    VALOR_FATURADO,
    TOTAL_UST,
    DATA_ULTIMA_ATUALIZACAO,
    USER_ABERTURA_ID,
    DATA_ABERTURA,
    APROVADA,
    USER_APROVADA_ID,
    ID_TAREFA_OLD,
    USER_ULTIMA_ATUALIZACAO_ID,
    TAREFA_PAI_ID,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_TAREFA
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

