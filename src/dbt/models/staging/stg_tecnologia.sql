    ID,
    NULLIF(TRIM(NOME), '') AS NOME,
    UNIDADE_ADM_ID,
    ID_TECNOLOGIA_OLD,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM {{ source('munka_raw', 'raw_tecnologia') }}
