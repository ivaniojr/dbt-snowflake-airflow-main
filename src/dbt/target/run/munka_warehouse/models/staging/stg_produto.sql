
  create or replace   view DRAGON_DB.munka_stg.stg_produto
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "NOME" COMMENT $$$$, 
  
    "UNIDADE_SUPERIOR_ID" COMMENT $$$$, 
  
    "UNIDADE_EXECUTANTE_ID" COMMENT $$$$, 
  
    "UNIDADE_ADM_ID" COMMENT $$$$, 
  
    "DATA_SOLICITACAO" COMMENT $$$$, 
  
    "LINK_GIT" COMMENT $$$$, 
  
    "DESCRICAO" COMMENT $$$$, 
  
    "LINK_HOMOLOG" COMMENT $$$$, 
  
    "LINK_PROD" COMMENT $$$$, 
  
    "INF_ADICIONAL" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    NULLIF(TRIM(NOME), '') AS NOME,
    UNIDADE_SUPERIOR_ID,
    UNIDADE_EXECUTANTE_ID,
    UNIDADE_ADM_ID,
    DATA_SOLICITACAO,
    NULLIF(TRIM(LINK_GIT), '') AS LINK_GIT,
    NULLIF(TRIM(DESCRICAO), '') AS DESCRICAO,
    NULLIF(TRIM(LINK_HOMOLOG), '') AS LINK_HOMOLOG,
    NULLIF(TRIM(LINK_PROD), '') AS LINK_PROD,
    NULLIF(TRIM(INF_ADICIONAL), '') AS INF_ADICIONAL,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_PRODUTO
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

