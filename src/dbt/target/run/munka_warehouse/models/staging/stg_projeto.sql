
  create or replace   view DRAGON_DB.munka_stg.stg_projeto
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "UNIDADE_SUPERIOR_ID" COMMENT $$$$, 
  
    "UNIDADE_ADM_ID" COMMENT $$$$, 
  
    "DATA_SOLICITACAO" COMMENT $$$$, 
  
    "DESCRICAO" COMMENT $$$$, 
  
    "NOME" COMMENT $$$$, 
  
    "PRODUTO_ID" COMMENT $$$$, 
  
    "ABRANGENCIA" COMMENT $$$$, 
  
    "ACESSO" COMMENT $$$$, 
  
    "NUMERO_SEI" COMMENT $$$$, 
  
    "DATA_PROTOTIPO" COMMENT $$$$, 
  
    "LINK_PROTOTIPO" COMMENT $$$$, 
  
    "DATA_DESENV" COMMENT $$$$, 
  
    "LINK_DESENV" COMMENT $$$$, 
  
    "DATA_HOMOLOG" COMMENT $$$$, 
  
    "DATA_PROD" COMMENT $$$$, 
  
    "STATUS_ID" COMMENT $$$$, 
  
    "PROGRESSO" COMMENT $$$$, 
  
    "OBS_PROGRESSO" COMMENT $$$$, 
  
    "JUSTIFICATIVA" COMMENT $$$$, 
  
    "INATIVO" COMMENT $$$$, 
  
    "DATA_PREVISAO_INICIO" COMMENT $$$$, 
  
    "DATA_PREVISAO_TERMINO" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    UNIDADE_SUPERIOR_ID,
    UNIDADE_ADM_ID,
    DATA_SOLICITACAO,
    NULLIF(TRIM(DESCRICAO), '') AS DESCRICAO,
    NULLIF(TRIM(NOME), '') AS NOME,
    PRODUTO_ID,
    NULLIF(TRIM(ABRANGENCIA), '') AS ABRANGENCIA,
    NULLIF(TRIM(ACESSO), '') AS ACESSO,
    NUMERO_SEI,
    DATA_PROTOTIPO,
    NULLIF(TRIM(LINK_PROTOTIPO), '') AS LINK_PROTOTIPO,
    DATA_DESENV,
    NULLIF(TRIM(LINK_DESENV), '') AS LINK_DESENV,
    DATA_HOMOLOG,
    DATA_PROD,
    STATUS_ID,
    PROGRESSO,
    NULLIF(TRIM(OBS_PROGRESSO), '') AS OBS_PROGRESSO,
    NULLIF(TRIM(JUSTIFICATIVA), '') AS JUSTIFICATIVA,
    INATIVO,
    DATA_PREVISAO_INICIO,
    DATA_PREVISAO_TERMINO,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_PROJETO
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

