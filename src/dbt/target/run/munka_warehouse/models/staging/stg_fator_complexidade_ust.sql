
  create or replace   view DRAGON_DB.munka_stg.stg_fator_complexidade_ust
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "FATOR_COMPLEXIDADE" COMMENT $$$$, 
  
    "CARGO_ID" COMMENT $$$$, 
  
    "NIVEL_ID" COMMENT $$$$, 
  
    "CONTRATO_ID" COMMENT $$$$, 
  
    "ATIVO" COMMENT $$$$, 
  
    "ID_FATOR_OLD" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    FATOR_COMPLEXIDADE,
    CARGO_ID,
    NIVEL_ID,
    CONTRATO_ID,
    ATIVO,
    ID_FATOR_OLD,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_FATOR_COMPLEXIDADE_UST
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

