WITH tarefa AS (
    SELECT
        ID AS TAREFA_ID,
        NOME,
        HASH('PROJETO', PROJETO_ID) AS SK_PROJETO,
        HASH('SPRINT', SPRINT_ID) AS SK_SPRINT,
        HASH('USUARIO', RESPONSAVEL_ID) AS SK_USUARIO,
        HASH('STATUS', STATUS_ID) AS SK_STATUS,
        HASH('REGRA', REGRA_ID) AS SK_REGRA,
        DATA_INICIO,
        DATA_FIM,
        HORAS_EXECUTADAS,
        TOTAL_UST
    FROM DRAGON_DB.munka_stg.stg_tarefa
),
evidencias AS (
    SELECT
        TAREFA_ID,
        QTD_IMAGENS,
        QTD_LINKS,
        TEM_CODIGO,
        TEM_SQL,
        TEM_COMMIT,
        TEM_ANEXO,
        FL_ENVOLVE_FRONTEND,
        FL_ENVOLVE_BACKEND,
        FL_ENVOLVE_DADOS,
        FL_IS_BUGFIX,
        QTD_BLOCOS_CODIGO,
        FL_TEM_PULL_REQUEST,
        TAMANHO_TEXTO,
        SCORE_QUALIDADE_EVIDENCIA
    FROM DRAGON_DB.munka_int.int_tarefa_evidencias_features
)

SELECT
    t.TAREFA_ID,
    t.NOME,
    t.SK_PROJETO,
    t.SK_SPRINT,
    t.SK_USUARIO,
    t.SK_STATUS,
    t.SK_REGRA,
    t.DATA_INICIO,
    t.DATA_FIM,
    t.HORAS_EXECUTADAS,
    t.TOTAL_UST,
    e.QTD_IMAGENS,
    e.QTD_LINKS,
    e.TEM_CODIGO,
    e.TEM_SQL,
    e.TEM_COMMIT,
    e.TEM_ANEXO,
    e.FL_ENVOLVE_FRONTEND,
    e.FL_ENVOLVE_BACKEND,
    e.FL_ENVOLVE_DADOS,
    e.FL_IS_BUGFIX,
    e.QTD_BLOCOS_CODIGO,
    e.FL_TEM_PULL_REQUEST,
    e.TAMANHO_TEXTO,
    e.SCORE_QUALIDADE_EVIDENCIA
FROM tarefa t
LEFT JOIN evidencias e
    ON t.TAREFA_ID = e.TAREFA_ID