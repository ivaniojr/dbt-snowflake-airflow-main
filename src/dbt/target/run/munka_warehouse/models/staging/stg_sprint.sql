
  create or replace   view DRAGON_DB.munka_stg.stg_sprint
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "DATA_INICIO" COMMENT $$$$, 
  
    "DATA_FIM" COMMENT $$$$, 
  
    "OBJETIVOS" COMMENT $$$$, 
  
    "COORDENACAO_ID" COMMENT $$$$, 
  
    "DESCRICAO_CURTA" COMMENT $$$$, 
  
    "ID_SPRINT_OLD" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    DATA_INICIO,
    DATA_FIM,
    NULLIF(TRIM(OBJETIVOS), '') AS OBJETIVOS,
    COORDENACAO_ID,
    NULLIF(TRIM(DESCRICAO_CURTA), '') AS DESCRICAO_CURTA,
    ID_SPRINT_OLD,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_SPRINT
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

