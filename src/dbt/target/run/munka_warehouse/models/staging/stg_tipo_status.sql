
  create or replace   view DRAGON_DB.munka_stg.stg_tipo_status
  
    
    
(
  
    "ID" COMMENT $$$$, 
  
    "STATUS_ID" COMMENT $$$$, 
  
    "TIPO_ID" COMMENT $$$$, 
  
    "ID_TIPO_STATUS_OLD" COMMENT $$$$, 
  
    "DW_BATCH_ID" COMMENT $$$$, 
  
    "DW_RECORD_SOURCE" COMMENT $$$$, 
  
    "DW_SOURCE_UPDATED_AT" COMMENT $$$$, 
  
    "DW_INGESTED_AT" COMMENT $$$$, 
  
    "DW_ROW_HASH" COMMENT $$$$
  
)

   as (
    SELECT
    ID,
    STATUS_ID,
    TIPO_ID,
    ID_TIPO_STATUS_OLD,
    DW_BATCH_ID,
    DW_RECORD_SOURCE,
    DW_SOURCE_UPDATED_AT,
    DW_INGESTED_AT,
    DW_ROW_HASH
FROM DRAGON_DB.MUNKA_RAW.RAW_TIPO_STATUS
QUALIFY ROW_NUMBER() OVER (PARTITION BY ID ORDER BY DW_INGESTED_AT DESC, DW_SOURCE_UPDATED_AT DESC NULLS LAST) = 1
  );

